package com.nexenio.rxpreferences;

public class RxPreferencesException extends Exception {

    public RxPreferencesException() {
    }

    public RxPreferencesException(String message) {
        super(message);
    }

    public RxPreferencesException(String message, Throwable cause) {
        super(message, cause);
    }

    public RxPreferencesException(Throwable cause) {
        super(cause);
    }

}
